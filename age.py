age=int(input("Enter your age:"))
if age>=50:
    print("You are above 50")
elif age>40 and age<50:
    print("You are between 40 and 50")
elif age>30 and age<40:
    print("You are between 30 and 40")
elif age>20 and age<30:
    print("You are between 20 and 30")
elif age>10 and age<20:
    print("You are between 10 and 20")
else:
    print("You are below 10")